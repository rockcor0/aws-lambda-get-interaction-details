package com.genesyscloud.interactiondetails.model;

public class Outcome {
	
	private String flowOutcomeId;
	private String flowOutcomeValue;
	private String flowOutcome;
	private String flowOutcomeStartTimestamp;
	private String flowOutcomeEndTimestamp;
	
	/**
	 * @return the flowOutcomeId
	 */
	public String getFlowOutcomeId() {
		return flowOutcomeId;
	}
	/**
	 * @param flowOutcomeId the flowOutcomeId to set
	 */
	public void setFlowOutcomeId(String flowOutcomeId) {
		this.flowOutcomeId = flowOutcomeId;
	}
	/**
	 * @return the flowOutcomeValue
	 */
	public String getFlowOutcomeValue() {
		return flowOutcomeValue;
	}
	/**
	 * @param flowOutcomeValue the flowOutcomeValue to set
	 */
	public void setFlowOutcomeValue(String flowOutcomeValue) {
		this.flowOutcomeValue = flowOutcomeValue;
	}
	/**
	 * @return the flowOutcome
	 */
	public String getFlowOutcome() {
		return flowOutcome;
	}
	/**
	 * @param flowOutcome the flowOutcome to set
	 */
	public void setFlowOutcome(String flowOutcome) {
		this.flowOutcome = flowOutcome;
	}
	/**
	 * @return the flowOutcomeStartTimestamp
	 */
	public String getFlowOutcomeStartTimestamp() {
		return flowOutcomeStartTimestamp;
	}
	/**
	 * @param flowOutcomeStartTimestamp the flowOutcomeStartTimestamp to set
	 */
	public void setFlowOutcomeStartTimestamp(String flowOutcomeStartTimestamp) {
		this.flowOutcomeStartTimestamp = flowOutcomeStartTimestamp;
	}
	/**
	 * @return the flowOutcomeEndTimestamp
	 */
	public String getFlowOutcomeEndTimestamp() {
		return flowOutcomeEndTimestamp;
	}
	/**
	 * @param flowOutcomeEndTimestamp the flowOutcomeEndTimestamp to set
	 */
	public void setFlowOutcomeEndTimestamp(String flowOutcomeEndTimestamp) {
		this.flowOutcomeEndTimestamp = flowOutcomeEndTimestamp;
	}
	
	

}
